import { Bounce, Flip, Slide, Zoom, toast } from "react-toastify";

export const  successToast = (message)=>{
    toast.success(message, {
        position: "bottom-center",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "dark",
        transition: Slide,
        });
}

